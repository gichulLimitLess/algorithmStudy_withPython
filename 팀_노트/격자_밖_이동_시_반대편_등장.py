# ───────────────────────────────
# [wrap-around 이동 템플릿]
# ───────────────────────────────

dy = [-1, 1, 0, 0]
dx = [0, 0, -1, 1]

def move_with_wrap(y, x, dir, k, n, m):
    """
    ▪️ 격자 밖으로 벗어나면 반대편으로 등장시키는 함수
    ▪️ n, m : 격자 크기
    ▪️ k : 이동 칸 수
    ▪️ (음수, 초과 모두 대응 가능)
    """
    ny = (y + dy[dir] * k) % n
    nx = (x + dx[dir] * k) % m
    return ny, nx


# ───────────────────────────────
# ✅ 예제 실행
# ───────────────────────────────
n, m = 4, 4
y, x = 0, 0

print(move_with_wrap(y, x, 2, 1, n, m))  # ← (0,3)
print(move_with_wrap(y, x, 0, 2, n, m))  # ↑ (2,0)
print(move_with_wrap(y, x, 3, 5, n, m))  # → (0,1)
