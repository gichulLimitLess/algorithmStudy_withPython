from collections import deque
import copy

# ë²½ ì•„ë˜ë¡œ í•˜ë‚˜ì”© ì˜®ê¸°ëŠ” í•¨ìˆ˜
def move_walls(board):
    new_board = [['.' for _ in range(8)] for _ in range(8)]
    for i in range(8):
        for j in range(8):
            if board[i][j] == '#':
                if i + 1 < 8:  # ì•„ë˜ë¡œ í•œ ì¹¸ ì´ë™ (ë§¨ ì•„ë˜ëŠ” ì‚¬ë¼ì§)
                    new_board[i + 1][j] = '#'
    return new_board


# ì…ë ¥ ì²˜ë¦¬
first_board = [list(input().strip()) for _ in range(8)]

# visited[y][x][time] í˜•íƒœì˜ 3ì°¨ì› ë°°ì—´ë¡œ visited ë°°ì—´ ê´€ë¦¬
visited = [[[False] * 9 for _ in range(8)] for _ in range(8)]
