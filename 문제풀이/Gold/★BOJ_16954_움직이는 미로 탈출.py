# ìš±ì œì˜ ì´ë™ ë°©í–¥ (8ë°©í–¥ + ì œìë¦¬)
dy = [0, 0, 1, -1, -1, 1, -1, 1, 0]
dx = [1, -1, 0, 0, 1, 1, -1, -1, 0]

q = deque()
q.append((7, 0, 0, copy.deepcopy(first_board)))
answer = 0

while q:
    y, x, time, board = q.popleft()

    # ğŸŸ¡ 1ï¸âƒ£ 8ì´ˆê°€ ì§€ë‚˜ë©´ ëª¨ë“  ë²½ì´ ì‚¬ë¼ì§„ ìƒíƒœ â†’ ìƒì¡´ ì„±ê³µ
    if time >= 8:
        answer = 1
        break

    # ğŸŸ¡ 2ï¸âƒ£ í˜„ì¬ ì¹¸ì´ ì´ë¯¸ ë²½ì´ë¼ë©´, ê¹”ë ¤ì„œ ì‚¬ë§
    if board[y][x] == '#':
        continue

    new_board = move_walls(copy.deepcopy(board))  # í•œ ë²ˆë§Œ ê³„ì‚°

    # ğŸŸ¡ 3ï¸âƒ£ í˜„ì¬ ì‹œì ì˜ ë²½ì„ ê¸°ì¤€ìœ¼ë¡œ ìš±ì œê°€ ì´ë™í•  ìˆ˜ ìˆëŠ” ì¹¸ íƒìƒ‰
    for i in range(9):
        ny, nx = y + dy[i], x + dx[i]
        nt = time + 1
        if not (0 <= ny < 8 and 0 <= nx < 8):
            continue

        # í˜„ì¬ ì‹œì ì˜ ë²½ì— ë§‰íˆë©´ ì´ë™ ë¶ˆê°€
        if board[ny][nx] == '#':
            continue

        # ë‹¤ìŒ í”„ë ˆì„ì—ì„œ ë²½ì´ ìš±ì œë¥¼ ë®ëŠ”ì§€ í™•ì¸
        if new_board[ny][nx] == '#':
            continue

        # ì´ë¯¸ ê°™ì€ (y, x, time) ë°©ë¬¸í–ˆë‹¤ë©´ skip
        if visited[ny][nx][min(nt, 8)]:
            continue

        visited[ny][nx][min(nt, 8)] = True
        q.append((ny, nx, nt, new_board))

print(answer)
